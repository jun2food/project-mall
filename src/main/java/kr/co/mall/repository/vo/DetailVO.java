package kr.co.mall.repository.vo;

public class DetailVO {
	private int ifNo;
	private int pNo;
	private String ifOri;
	private String ifReal;
	private String ifPath;
	
	
	public int getIfNo() {
		return ifNo;
	}
	public void setIfNo(int ifNo) {
		this.ifNo = ifNo;
	}
	public int getpNo() {
		return pNo;
	}
	public void setpNo(int pNo) {
		this.pNo = pNo;
	}
	public String getIfOri() {
		return ifOri;
	}
	public void setIfOri(String ifOri) {
		this.ifOri = ifOri;
	}
	public String getIfReal() {
		return ifReal;
	}
	public void setIfReal(String ifReal) {
		this.ifReal = ifReal;
	}
	public String getIfPath() {
		return ifPath;
	}
	public void setIfPath(String ifPath) {
		this.ifPath = ifPath;
	}
	
	
}
